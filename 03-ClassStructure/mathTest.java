public class mathTest {
    public static void main(String[] args) {
        
        System.out.println("Larger number from 34 and 49 is: " + Math.max(34,49));

        System.out.println("Pi number: " + Math.PI);

        System.out.println("absolute value of -17: " + Math.abs(-17));

        System.out.println("Square root of the expression: nine divided by two: " + Math.sqrt(9f/2f) );

        System.out.println("Random number in the range <0.0,1.0>: " + Math.random());

        System.out.println("Random number in the range <0,10>: " + Math.round(Math.random()*10));

        System.out.println("Sine of 90 degrees: " + Math.sin(90));
    }
}
